struct CRC16_type
{
	byte result_high;
	byte result_low;
	byte data_in;
};

struct Modbus_Type
{
	byte iData_Input;    //当前输入的数据
	byte iDevice_addr;   //设备地址
	byte iFunction_code; //功能码
	byte iData_len;      //数据长度
	long bzData[100];    //保存数据
	byte iIndex;         //数据索引
	int iStatus;         //接收的状态
};

struct Motor_Info
{
	int iModbus_enable;        //modbus控制使能，1使能，使能后PU,EN,DIR失效
	int iEN_enable;            //modbus控制 0:禁止    1:使能
	int iTarget_speed;         //目标转速 单位 r/min
	int iAcceleration;         //电机加速度
	int iSpeed_start;          //电机起始转速
	int iSpeed_kp;             //速度控制KP参数，范围0到10000代表0.0~10.0倍
	int iSpeed_ki;             //速度控制Ki参数 0~2000 代表0到2S的积分时间
	int iPosition_kp;          //位置控制Kp参数 60~3000  (r/min)/r
	int iSpeed_feedforward;    //速度前馈
	int iDIR_polarity;         //加速度前馈
	int iE_Gear_numerator;     //电子齿轮分子
	int iE_Gear_denominator;   //电子齿轮分母

	int iPU;                   //PU步数
	int iWaring_code;          //报警代码
	int iSystem_DC_current;    //系统的直流电流
	int iSpeed_actual;         //当前速度 单位r/min
	int iSystem_voltage;       //系统电压
	int iSystem_temperature;   //系统温度
	int iSystem_pwm;           //系统输出的PWM
	int iModbus_save_flag;     //保存标志  0:不需保存 1: 需要保存  2:保存完毕
	int iDevice_addr;
	int iPU_all;               //PU总步长
	int iSpeed_filter;         //速度滤波频率
	int iPosition_feedforward; //位置前馈
};

